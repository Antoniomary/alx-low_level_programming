/**
 * _strlen - returns the length of a string.
 * @*s: a pointer to variable s which contains the string
 * to be counted.
 * @s: the array variable with the string.
 * Return: lenght
 */
int _strlen(char *s)
{
	int lenght = 0;
	int n;

	for (n = 0; n <= s[n]; n++)
	{
		lenght++;
	}
	return (lenght);
}
